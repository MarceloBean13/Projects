#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long int n;

	n = nb;
	if (nb < 0)
	{
		putchar('-');
		n = -n;
	}
	if (n / 10)
		ft_putnbr(n / 10);
	putchar(n % 10 + '0');
}
