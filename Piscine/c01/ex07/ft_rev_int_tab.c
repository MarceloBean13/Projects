void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int tab2;

	i = 0;
	while (i < size)
	{
		tab2 = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = tab2;
		i++;
		size--;
	}
}
