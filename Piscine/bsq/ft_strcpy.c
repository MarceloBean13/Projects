#include "BSQ.h"

char	*ft_strcpy(char *str1, char *str2)
{
	int i;

	i = 0;
	while (str2[i] != '\0')
	{
		str1[i] = str2[i];
		i++;
	}
	str1[i] = str2[i];
	return (str1);
}
